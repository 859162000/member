package com.wanda.ccs.member.segment;

public class SegmentSysException extends RuntimeException {

	private static final long serialVersionUID = 7419652718134221121L;

	public SegmentSysException() {
		super();
	}

	public SegmentSysException(String message, Throwable cause) {
		super(message, cause);
	}

	public SegmentSysException(String message) {
		super(message);
	}

	public SegmentSysException(Throwable cause) {
		super(cause);
	}

}
